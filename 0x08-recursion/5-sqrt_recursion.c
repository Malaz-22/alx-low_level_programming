/**
 * _sqrt_recursion - returns the natural square root of a number
 * @n: number which will need square root
 * Return: natural square root of a number
 */
int _sqrt_recursion(int n)
{
	int i;

	if (n != (i * i))
	{
		return (-1);
	}
	if (n < 0)
	{
		return (-1);
	}
	if (n == (i * i))
	{
		return (i);
	}
}
